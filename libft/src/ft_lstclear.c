#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*temp;

	while (*lst)
	{
		temp = (*lst)->next;
		ft_lstdelone(*lst, del);
		*lst = temp;
	}
}

// COMMENTS:

// void (*del)(void*) = een template. omdat het een pointer is
// kan je elke functie hierin plaatsen. maw een place holder.
